	JMP LOOP

	CONFIGURARHORAALARMA:
		CALL CONFIGURARHORAALARMA_L
		CALL ACTUALIZAR_AL_RELOJ
	JMP LOOP

JMP LOOP



//******************************************************************************
//SUB RUTINA
//******************************************************************************
RELOJ:
// FUNCIONA COMO UN RELOJ DE ENGRANAJE, CADA QUE AUMENTA EL SEGUNDO COMPARA HASTA EL MINUTO Y SUCESIVAMENTE
	CPI SEGUNDOS, 60
	BRGE MINU
	RET
	MINU:
	LDI SEGUNDOS, 0
	INC MINUTOS
	CALL ALARMA
	CPI MINUTOS, 60
	BREQ HOR
	RET
	HOR:
	LDI MINUTOS, 0
	INC HORAS
	CALL ALARMA
	CPI HORAS, 24
	BREQ DI
	RET
	DI:
	LDI HORAS, 0
	CALL ALARMA
	INC DIAS
	CALL CALENDARIO ; SUBRUTINA DEDICADA A DEFINIR CANTIDAD DE DIAS POR MES Y DEFENSIVA POR VALORES MENORES A 0
RET


CALENDARIO:
