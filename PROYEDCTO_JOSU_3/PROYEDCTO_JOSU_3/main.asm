ISR_PCINT1:
PUSH R16
IN R16, SREG
PUSH R16
	SBIS PINC, PINC2 ; CAMBIAR MODO
	INC MODO
	CPI MODO, 8
	IN R16, SREG
	SBRC R16, SREG_Z
	LDI MODO, 0
	SBIS PINC, PINC3; APAGAR ALARMA 
	JMP APAGAR_ALARMA
	CPI MODO, 0
	BREQ MHO ; MODO HORA
	CPI MODO, 1
	BREQ MFE ; MODO FECHA
	CPI MODO, 2
	BREQ MCMIN ; MODO CONFIGURAR MINUTOS
	CPI MODO, 3
	BREQ MCHOR ; MODO CONFIGURAR HORA
	CPI MODO, 4
	BREQ MCFEC ; MODO CONFIGURAR FECHA
	CPI MODO, 5
	BREQ MCME ; MODO CONFIGURAR MES
	CPI MODO, 6
	BREQ MCALM ; MODO CONFIGURAR ALARMA MINUTO
	CPI MODO, 7
	IN R16, SREG
	SBRC R16, SREG_Z
	JMP MCALH ; MODO CONFIGURAR ALARMA HORA
	JMP SALIDAPC1	
		MHO:
			CBI PORTC, PORTC4
			CBI PORTC, PORTC5
			CBI PORTB, PORTB5
			JMP SALIDAPC1		
		MFE:
			CBI PORTC, PORTC4
			CBI PORTC, PORTC5
			SBI PORTB, PORTB5
			JMP SALIDAPC1		
		MCMIN:
			CBI PORTC, PORTC4
			SBI PORTC, PORTC5
			CBI PORTB, PORTB5
			MOV CONFMINUTOS, MINUTOS
			SBIS PINC, PINC0
			INC CONFMINUTOS
			SBIS PINC, PINC1
			DEC CONFMINUTOS
			MOV MINUTOS, CONFMINUTOS
			JMP SALIDAPC1		
		MCHOR:
			CBI PORTC, PORTC4
			SBI PORTC, PORTC5
			SBI PORTB, PORTB5
			MOV CONFHORAS, HORAS
			SBIS PINC, PINC0
			INC CONFHORAS
			SBIS PINC, PINC1
			DEC CONFHORAS
			MOV HORAS, CONFHORAS
			JMP SALIDAPC1
		MCFEC:
			SBI PORTC, PORTC4
			CBI PORTC, PORTC5
			CBI PORTB, PORTB5
			MOV CONFDIA, DIAS
			SBIS PINC, PINC0
			INC CONFDIA
			SBIS PINC, PINC1 
			DEC CONFDIA
			MOV DIAS, CONFDIA
			JMP SALIDAPC1
		MCME:
			SBI PORTC, PORTC4
			CBI PORTC, PORTC5
			SBI PORTB, PORTB5
			MOV CONFMES, MESES
			SBIS PINC, PINC0
			INC CONFMES
			SBIS PINC, PINC1 
			DEC CONFMES
			MOV MESES, CONFMES
			JMP SALIDAPC1
		MCALM:
			SBI PORTC, PORTC4
			SBI PORTC, PORTC5
			CBI PORTB, PORTB5
			MOV ALMINUTO, CONFMINUTOS
			SBIS PINC, PINC0
			INC ALMINUTO
