MAIN:
//******************************************************************************
//Configuración de la pila
//******************************************************************************
LDI R16, LOW(RAMEND)
OUT SPL, R16
LDI R17, HIGH(RAMEND)
OUT SPH, R17
//******************************************************************************


//******************************************************************************
CONFIGURACIONES:
//******************************************************************************
SETUP: 
// PRESCALER
	LDI R16, (1<<CLKPCE)
	STS CLKPR, R16
	LDI R16, 0x05
	STS CLKPR, R16 
// CONFIGURAR ENTRADAS Y SALIDAS
	LDI R16, 0x00
	STS UCSR0B, R16
	LDI R16, 0xFF
	OUT DDRD, R16 ; DEFINIR EL PUERTO D COMO SALIDA
	LDI R16, 0 
	OUT PORTD, R29 ; COLOCAR EN 0 EL PUERTO D
	LDI R16, 0x3F ; DEFINIR EL PUERTO B COMO SALIDA
	OUT DDRB, R16 
	LDI R16, 0; COLOCAR EN 0 EL PUERTO B
	OUT PORTB, R16
	LDI R16, 0b110000
	OUT DDRC, R16; DEFINIR PUERTO C COMO ENTRADA Y SALIDA
	LDI R16, 0b001111
	OUT PORTC, R16 ; COLOCAR PULLUPS


// INICIALIZAR VARIABLES
	LDI MODO, 0
	LDI SEGUNDOS, 0
	LDI MINUTOS, 0
	LDI HORAS, 0
	LDI DIAS, 1
// CONFIGURAR INTERRUPCION DE TIMERS
CALL TIMER0
// CONFIGURAR INTERRUPCIONES DE BOTONES EN PUERTO C
	LDI R16, 0b00001111
	STS PCMSK1, R16
	LDI R16, 0b00000010
	STS PCICR, R16
	SEI
//******************************************************************************
//Loop Infinito
//******************************************************************************
LOOP:
	CPI MODO, 0
		BREQ DIAYHORA
	CPI MODO, 1
		BREQ FECHAYMES
	CPI MODO, 2
		BREQ CONFIGURARMINUTOS
	CPI MODO, 3
		BREQ CONFIGURARHORA
	CPI MODO, 4
		BREQ CONFIGURARFECHA
	CPI MODO, 5
		BREQ CONFIGURARMES
	CPI MODO, 6
		BREQ CONFIGURARMINUTOALARMA
	CPI MODO, 7
		BREQ CONFIGURARHORAALARMA
	JMP LOOP

	DIAYHORA:
		CALL RELOJ
		CALL ACTUALIZAR_RELOJ
	JMP LOOP

	FECHAYMES:
		CALL RELOJ
		CALL CALENDARIO
		CALL ACTUALIZAR_FECHA
	JMP LOOP

	CONFIGURARMINUTOS:
		CALL CONFIGURARMINUTOS_L
		CALL ACTUALIZAR_RELOJ
	JMP LOOP
	
	CONFIGURARHORA:
		CALL CONFIGURARHORAS_L
		CALL ACTUALIZAR_RELOJ
	JMP LOOP
	
	CONFIGURARFECHA:
		CALL CONFIGURARFECHA_L
		CALL ACTUALIZAR_FECHA
	JMP LOOP

	CONFIGURARMES:
		CALL CONFIGURARMES_L
		CALL ACTUALIZAR_FECHA
