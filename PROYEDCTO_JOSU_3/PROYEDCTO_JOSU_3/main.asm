	CPI MESES, 9
	BREQ TREINTAYUNO
	CPI MESES, 10
	BREQ TREINTA
	CPI MESES, 11
	BREQ TREINTAYUNO

		TREINTAYUNO:
		CPI DIAS, 32
		BRGE REINICIARTREINAYUNO
		RET
		REINICIARTREINAYUNO:
		LDI DIAS, 1 
		INC MESES
		CPI MESES, 12
		BREQ REINICIARANO
		RET
		REINICIARANO:
		LDI MESES, 0 
		RET

		TREINTA:
		CPI DIAS, 31
		BRGE REINICIARTREINTA
		RET
		REINICIARTREINTA:
		LDI DIAS, 1 
		INC MESES
		RET
	
		VEINTIOCHO:
		CPI DIAS, 29
		BRGE REINICIARVEINTIOCHO
		RET
		REINICIARVEINTIOCHO:
		LDI DIAS, 1 
		INC MESES
RET



ALARMA: 
// COMPARA MINUTOS CON LA ALARMA Y SI SON IGUALES COMPARA LA HORA Y SI CUMPLE ENCIENDE LA ALARMA
	CPSE MINUTOS, ALMINUTO
	RET
	CPSE HORAS, ALHORA
	RET
	SBI PINB, PINB0
RET



